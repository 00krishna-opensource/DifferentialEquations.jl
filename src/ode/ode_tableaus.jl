"""
ExplicitRKTableau

Holds a tableau which defines an explicit Runge-Kutta method.
"""
type ExplicitRKTableau <: Tableau
  A#::Array{Float64,2}
  c#::Vector{Float64}
  α#::Vector{Float64}
  αEEst#::Vector{Float64}
  stages::Int
  order::Int
  adaptiveorder::Int #The lower order of the pair. Only used for adaptivity.
  ExplicitRKTableau(A,c,α,order;adaptiveorder=0,αEEst=Float64[]) = new(A,c,α,αEEst,length(α),order,adaptiveorder)
end

"""
Base.length(tab::ExplicitRKTableau)

Defines the length of a Runge-Kutta method to be the number of stages.
"""
Base.length(tab::ExplicitRKTableau) = tab.stages

"""
constructHuen()

Constructs the tableau object for Huen's Order 2 method.
"""
function constructHuen(T::Type = Float64)
  A = [0 0
       1 0]
  c = [0;1]
  α = [1//2;1//2]
  αEEst = [1;0]
  A = map(T,A)
  α = map(T,α)
  c = map(T,c)
  αEEst = map(T,αEEst)
  return(ExplicitRKTableau(A,c,α,2,αEEst=αEEst,adaptiveorder=1))
end
"""
constructRalston()

